def generate_inline_diff(
    markdown_content: str,
    result: ValidationResult,
    course_name: str = "Course"
) -> str:
    """
    Generate inline diff output showing changes in track-changes style.

    Args:
        markdown_content: Original converted markdown content
        result: ValidationResult with all issues
        course_name: Name of the course for header

    Returns:
        Markdown string with inline changes marked up
    """
    lines = []

    # Header
    lines.append(f"# Inline Editorial Review: {course_name}")
    lines.append("")
    lines.append("**Instructions**: Search for `~~` to find deletions and `**` to find additions.")
    lines.append("Copy this document into Word to see Track Changes formatting.")
    lines.append("")
    lines.append("---")
    lines.append("")

    # Summary stats
    score = result.quality_score
    if score:
        lines.append(f"**Total Changes**: {score.total_issues} "
                    f"(Auto-fix: {score.safe_count}, Review: {score.review_count}, "
                    f"Questions: {score.query_count})")
        lines.append("")
        lines.append("---")
        lines.append("")

    # Apply changes to content
    modified_content = apply_inline_changes(markdown_content, result.issues)
    lines.append(modified_content)

    # Footer with legend
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("## Change Legend")
    lines.append("")
    lines.append("| Markup | Meaning |")
    lines.append("|--------|---------|")
    lines.append("| ~~text~~ | Text to remove (strikethrough) |")
    lines.append("| **text** | Text to add (bold) |")
    lines.append("| [Explanation: ...] | Rationale for change |")
    lines.append("")
    lines.append(f"*Generated by Course AI Editor on {datetime.now().strftime('%Y-%m-%d %H:%M')}*")

    return '\n'.join(lines)


def apply_inline_changes(content: str, issues: List[EditorialIssue]) -> str:
    """
    Apply inline markup to content showing all changes.

    Changes are applied in reverse order (by position) to maintain
    correct offsets as we modify the string.

    Args:
        content: Original markdown content
        issues: List of editorial issues with suggested fixes

    Returns:
        Modified content with inline change markup
    """
    # Filter to issues that have both original text and a suggestion
    applicable_issues = [
        i for i in issues
        if i.suggested_fix and i.original_text and i.suggested_fix != i.original_text
    ]

    # Sort by absolute_offset in reverse order
    # This ensures we apply changes from end to start to maintain offsets
    sorted_issues = sorted(applicable_issues, key=lambda x: x.absolute_offset, reverse=True)

    # Track which offsets we've already modified (to handle duplicates)
    modified_offsets = set()

    for issue in sorted_issues:
        # Skip if we've already modified this position
        if issue.absolute_offset in modified_offsets:
            continue

        # Find the text in content
        # Use absolute_offset if available, otherwise search
        if issue.absolute_offset > 0:
            start = issue.absolute_offset
            end = start + len(issue.original_text)

            # Verify the text matches
            if content[start:end] != issue.original_text:
                # Text doesn't match at expected position, try to find it
                start = content.find(issue.original_text, max(0, start - 50))
                if start == -1:
                    continue
                end = start + len(issue.original_text)
        else:
            # Search for the text
            start = content.find(issue.original_text)
            if start == -1:
                continue
            end = start + len(issue.original_text)

        # Build the inline change markup
        category = get_kim_category(issue.rule_id)
        rationale = get_short_rationale(issue)

        # Format: ~~original~~ **suggested** [Explanation: reason. Category: cat]
        markup = f"~~{issue.original_text}~~ **{issue.suggested_fix}** [Explanation: {rationale}. Category: {category}]"

        # Apply the change
        content = content[:start] + markup + content[end:]

        # Mark this offset as modified
        modified_offsets.add(issue.absolute_offset)

    # Also add QUERY issues as comments (no change, just question)
    query_issues = [i for i in issues if i.fix_type == 'QUERY']
    for issue in sorted(query_issues, key=lambda x: x.absolute_offset, reverse=True):
        if issue.absolute_offset in modified_offsets:
            continue

        if issue.absolute_offset > 0:
            start = issue.absolute_offset
            end = start + len(issue.original_text)

            if content[start:end] == issue.original_text:
                category = get_kim_category(issue.rule_id)
                markup = f"{issue.original_text} [QUESTION: {issue.message}. Category: {category}]"
                content = content[:start] + markup + content[end:]
                modified_offsets.add(issue.absolute_offset)

    return content


