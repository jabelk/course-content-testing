"""
Inline Diff Generator

Generates track-changes style output for course editorial review.
Uses markdown formatting that can be pasted into Word for Track Changes view.

Output format:
    ~~original~~ **suggested** [Explanation: reason. Category: category]

This format allows Kim to:
1. See changes inline in context
2. Search for ~~ or ** to find all changes
3. Copy/paste into Word where formatting is preserved
"""
