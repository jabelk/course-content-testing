# Category Mapping for Kim's 4 Categories
# =============================================================================

def get_kim_category(rule_id: str) -> str:
    """
    Map rule ID to Kim's 4 categories.

    Categories:
    - Cisco Style Guide
    - Acronyms
    - Technical Terms
    - Grammar & Punctuation
    """
    # Map rule prefixes to categories
    if rule_id.startswith('ACRONYM'):
        return "Acronyms"
    elif rule_id.startswith('PRODUCT') or rule_id.startswith('CODE'):
        return "Technical Terms"
    elif rule_id.startswith('TERM') or rule_id.startswith('HEADING') or rule_id.startswith('GUI'):
        return "Cisco Style Guide"
    elif rule_id.startswith('PUNCT') or rule_id.startswith('CMS') or rule_id.startswith('STRUCT'):
        return "Grammar & Punctuation"
    else:
        return "Grammar & Punctuation"


def get_short_rationale(issue: EditorialIssue) -> str:
    """
    Extract a concise rationale from the issue message.
    Removes redundant prefixes and keeps it brief.
    """
    msg = issue.message

    # Remove common prefixes
    prefixes = [
        "Consider ",
        "Found: ",
        "Detected: ",
        "Use ",
        "Change ",
    ]
    for prefix in prefixes:
        if msg.lower().startswith(prefix.lower()):
            msg = msg[len(prefix):]
            break

    # Truncate if too long
    if len(msg) > 60:
        msg = msg[:57] + "..."

    return msg


