# Alternative: Change List with Context
# =============================================================================

def generate_change_list_with_context(
    result: ValidationResult,
    course_name: str = "Course"
) -> str:
    """
    Generate a change list showing each change with surrounding context.

    This is an alternative format that shows:
    - Line number
    - Context before/after
    - The change in track-changes format
    - Category and rationale

    Args:
        result: ValidationResult with all issues
        course_name: Name of the course for header

    Returns:
        Markdown string with change list
    """
    lines = []

    # Header
    lines.append(f"# Editorial Change List: {course_name}")
    lines.append("")

    # Summary
    score = result.quality_score
    if score:
        lines.append(f"**Total Issues**: {score.total_issues}")
        lines.append(f"- Auto-fixable (SAFE): {score.safe_count}")
        lines.append(f"- Needs Review (REVIEW): {score.review_count}")
        lines.append(f"- Questions (QUERY): {score.query_count}")
        lines.append("")

    # Group by category
    by_category: Dict[str, List[EditorialIssue]] = {
        'Cisco Style Guide': [],
        'Acronyms': [],
        'Technical Terms': [],
        'Grammar & Punctuation': []
    }

    for issue in result.issues:
        category = get_kim_category(issue.rule_id)
        by_category[category].append(issue)

    # Output each category
    for category, issues in by_category.items():
        if not issues:
            continue

        lines.append(f"## {category} ({len(issues)} issues)")
        lines.append("")

        for issue in sorted(issues, key=lambda x: x.line_number):
            lines.append(f"### Line {issue.line_number}")
            lines.append("")

            # Show context with change inline
            context_before = issue.context_before or ""
            context_after = issue.context_after or ""

            if issue.suggested_fix and issue.original_text != issue.suggested_fix:
                # Show as inline change
                change_display = f"...{context_before}~~{issue.original_text}~~ **{issue.suggested_fix}**{context_after}..."
            else:
                # Show as question
                change_display = f"...{context_before}**{issue.original_text}**{context_after}..."

            lines.append(f"> {change_display}")
            lines.append("")

            # Type indicator
            type_emoji = {"SAFE": "🟢", "REVIEW": "🟡", "QUERY": "🔴"}.get(issue.fix_type, "⚪")
            lines.append(f"{type_emoji} **{issue.fix_type}**: {issue.message}")
            lines.append("")

    # Footer
    lines.append("---")
    lines.append(f"*Generated by Course AI Editor on {datetime.now().strftime('%Y-%m-%d %H:%M')}*")

    return '\n'.join(lines)


