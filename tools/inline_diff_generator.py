if __name__ == '__main__':
    import argparse
    import json

    parser = argparse.ArgumentParser(
        description='Generate inline diff from validation JSON'
    )
    parser.add_argument('json_file', help='Path to validation JSON file')
    parser.add_argument('markdown_file', help='Path to converted markdown file')
    parser.add_argument('--output', '-o', help='Output file (default: stdout)')
    parser.add_argument('--list-format', action='store_true',
                        help='Use change list format instead of inline diff')

    args = parser.parse_args()

    # Load validation result
    with open(args.json_file, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Load markdown content
    with open(args.markdown_file, 'r', encoding='utf-8') as f:
        markdown_content = f.read()

    # Reconstruct ValidationResult
    from editorial_models import QualityScore

    result = ValidationResult(tutorial_folder=data.get('tutorial_folder', 'Unknown'))
    result.files_processed = data.get('files_processed', [])
    result.processing_time_ms = data.get('processing_time_ms', 0)

    # Reconstruct issues
    for issue_data in data.get('issues', []):
        issue = EditorialIssue(
            rule_id=issue_data['rule_id'],
            file_path=issue_data['file_path'],
            line_number=issue_data['line_number'],
            original_text=issue_data['original_text'],
            fix_type=issue_data['fix_type'],
            message=issue_data['message'],
            confidence=issue_data['confidence'],
            suggested_fix=issue_data.get('suggested_fix'),
            context_before=issue_data.get('context_before', ''),
            context_after=issue_data.get('context_after', ''),
            absolute_offset=issue_data.get('absolute_offset', 0)
        )
        result.add_issue(issue)

    result.calculate_score()

    # Generate output
    course_name = os.path.splitext(os.path.basename(args.markdown_file))[0]

    if args.list_format:
        output = generate_change_list_with_context(result, course_name)
    else:
        output = generate_inline_diff(markdown_content, result, course_name)

    if args.output:
        with open(args.output, 'w', encoding='utf-8') as f:
            f.write(output)
        print(f"Output written to: {args.output}")
    else:
        print(output)
